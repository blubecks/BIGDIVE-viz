d3.select('#myCanvas')
    .attr('width', 600)
    .attr('height', 600)
    .append('rect')
    .attr('width', 100)
    .attr('height', 100)
    .transition() //the animation will be performed from 100 to 10
    .duration(3000)
    .delay(2000)
    .ease('bounce')
    .attr('height', 10)
    .attr('width', 200)
    .style('fill', 'red')
    .transition()
    .duration(3000)
    .delay(2000)
    .ease('bounce')
    .attr('height', 100)
    .attr('width', 100)
    .style('fill', 'black')